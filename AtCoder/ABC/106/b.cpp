#include <bits/stdc++.h>

using namespace std;

int main(){
    cin.tie(0);
    ios_base::sync_with_stdio(0);
    int n;
    cin >> n;
    if(n < 105) cout << "0\n";
    else if(n < 135)    cout << "1\n";
    else if(n < 165)    cout << "2\n";
    else if(n < 189)    cout << "3\n";
    else if(n < 195)    cout << "4\n";
    else    cout << "5\n";




    return 0;
}